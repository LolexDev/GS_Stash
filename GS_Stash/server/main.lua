local stash = {
    id = 'sef',
    label = 'Sef',
    slots = 50,
    weight = 100000,
    owner = 'char1:license'
}
 
AddEventHandler('onServerResourceStart', function(resourceName)
    if resourceName == 'ox_inventory' or resourceName == GetCurrentResourceName() then
        exports.ox_inventory:RegisterStash(stash.id, stash.label, stash.slots, stash.weight, stash.owner)
    end
end)

ESX.RegisterUsableItem(Config.Sef['sef'].SafeItem, function(source)
    local igrac = ESX.GetPlayerFromId(source)
    TriggerClientEvent("GS_Stash:postavi", source)
end)

RegisterNetEvent('GS_Stash:postavioS', function (item)
    local igrac = ESX.GetPlayerFromId(source)
    if igrac then
        igrac.removeInventoryItem(item, 1)
    end
end)

RegisterNetEvent('GS_Stash:sklonioSef', function (item)
    local igrac = ESX.GetPlayerFromId(source)
    if igrac then
        igrac.addInventoryItem(item, 1)
    end
end)

function discordlogs(name, message)
    local vreme = os.date("*t")
    local poruka = {
        {
            ["color"] = 2061822,
            ["title"] = "**".. name .."**",
            ["description"] = message,
            ["footer"] = {
                ["text"]= "Vreme: " .. vreme.hour .. ":" .. vreme.min .. ":" .. vreme.sec, -- Time
            },
        }
      }
    PerformHttpRequest(Config.Webhook, function(err, text, headers) end, 'POST', json.encode({username = Config.LogHeader, embeds = poruka, avatar_url = ""}), { ['Content-Type'] = 'application/json' })
end